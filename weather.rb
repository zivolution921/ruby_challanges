fav_weather = 'winter'

case fav_weather
 when 'cold'
 	puts "Buy a jacket, scarf, and gloves"
 when 'snowing'
 	puts "dress very warm and go snowboarding in mountain"
 when 'raining'
 	puts "get umbrella, dress warm and stay indoors"
 when 'undecided'
 	puts "dress with shorts sleeves but take a jacket with you just in case"
 when 'summer'
 puts "get pair of sneakers shorts sleeves and swimming suit and go jogging in the mountain and than you can go swimming"
 	else
 		puts "Just be ready for anything we can not controll mother earth"
 end 